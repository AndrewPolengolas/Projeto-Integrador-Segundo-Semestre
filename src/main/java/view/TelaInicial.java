    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String senha = new String(txtSenha.getPassword()).trim();
        
        while (!(Validacoes.ValidarCampo(txtUsuario.getText()) &&
                Validacoes.ValidarCampo(senha))) {
            txtUsuario.setBackground(Color.red);
            txtSenha.setBackground(Color.red);
            return;
        }
        
        ArrayList<Vendedor> listaVendedores = VendedorController.ConsultarVendedores();
        
        senha = new String(txtSenha.getPassword()).trim();
        
        for (Vendedor v : listaVendedores) {
            try{
               if(v.getNome().equals(txtUsuario.getText()) && v.getSenha().equals(senha)){
                    JOptionPane.showMessageDialog(null, "Bem-Vindo " + txtUsuario.getText(), "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                    TelaNavegacao telaNavegacao = new TelaNavegacao();
                    telaNavegacao.setVisible(true);
                    telaNavegacao.setLocationRelativeTo(null);
                    dispose();
                    break; 
                }
            }catch(HeadlessException e){
                JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

