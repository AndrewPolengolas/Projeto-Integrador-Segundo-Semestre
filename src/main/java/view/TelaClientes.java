    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(PesquisaNome.isSelected() && !(txtNome.getText().equals(""))){
            ArrayList<Cliente> listaClientes = ClienteController.ConsultarClientesNome(txtNome.getText());
            
            DefaultTableModel modelTabelClientes = new DefaultTableModel();
            modelTabelClientes.addColumn("CPF");
            modelTabelClientes.addColumn("Nome:");
            modelTabelClientes.addColumn("Email");
            modelTabelClientes.addColumn("Telefone");
            TabelaClientes.setModel(modelTabelClientes);

            modelTabelClientes.setRowCount(0);

            for (Cliente c : listaClientes) {
                modelTabelClientes.addRow(new Object[]{c.getCpf(),c.getNome(),c.getEmail(), c.getTelefone()});
            }

            txtNome.setText("");
            txtCpf.setText("");             
        }else if(PesquisaCpf.isSelected() && !(txtCpf.getText().equals(""))){
           
            ArrayList<Cliente> listaClientes = ClienteController.ConsultarClienteCpf(txtCpf.getText());
            
            DefaultTableModel modelTabelClientes = new DefaultTableModel();
            modelTabelClientes.addColumn("CPF");
            modelTabelClientes.addColumn("Nome:");
            modelTabelClientes.addColumn("Email");
            modelTabelClientes.addColumn("Telefone");
            TabelaClientes.setModel(modelTabelClientes);

            modelTabelClientes.setRowCount(0);

            
            for (Cliente c : listaClientes) {
                modelTabelClientes.addRow(new Object[]{c.getCpf(),c.getNome(),c.getEmail(), c.getTelefone()});
            }
            
            
            txtNome.setText("");
            txtCpf.setText("");
        }else{
            ArrayList<Cliente> listaClientes = ClienteController.ConsultarClientes();
            
            DefaultTableModel modelTabelClientes = new DefaultTableModel();
            modelTabelClientes.addColumn("CPF");
            modelTabelClientes.addColumn("Nome:");
            modelTabelClientes.addColumn("Email");
            modelTabelClientes.addColumn("Telefone");
            TabelaClientes.setModel(modelTabelClientes);

            modelTabelClientes.setRowCount(0);

            for (Cliente c : listaClientes) {
                modelTabelClientes.addRow(new Object[]{c.getCpf(),c.getNome(),c.getEmail(), c.getTelefone()});
            }

            txtNome.setText("");
            txtCpf.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(TabelaClientes.getSelectedRow() != -1){
            int numeroLinha = TabelaClientes.getSelectedRow();
            
            String cpf = TabelaClientes.getModel().getValueAt(numeroLinha, 0).toString();
            
            if(ClienteController.Excluir(cpf)){
                JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!");
                carregarTabela();
            }else{
                JOptionPane.showMessageDialog(this, "Falha o excluir cliente!");
            }     
        }else{
            JOptionPane.showMessageDialog(this, "Selecione um cliente da tabela!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(TabelaClientes.getSelectedRow() != -1){
            int numeroLinha = TabelaClientes.getSelectedRow();
            
            String cpf = TabelaClientes.getModel().getValueAt(numeroLinha, 0).toString();
            
            ArrayList<Cliente> listaClientes = ClienteController.ConsultarClienteCpf(cpf);
            
            TelaAlterarClientes telaAlterarClientes = new TelaAlterarClientes(listaClientes.get(0));
            telaAlterarClientes.setVisible(true);
            telaAlterarClientes.setLocationRelativeTo(null);
            dispose();
              
        }else{
            JOptionPane.showMessageDialog(this, "Selecione um cliente da tabela!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

