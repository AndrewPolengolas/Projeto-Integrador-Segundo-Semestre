    public void carregarTabela(){
        ArrayList<Cliente> listaClientes = ClienteController.ConsultarClientes();
            
            DefaultTableModel modelTabelClientes = new DefaultTableModel();
            modelTabelClientes.addColumn("CPF");
            modelTabelClientes.addColumn("Nome:");
            modelTabelClientes.addColumn("Email");
            modelTabelClientes.addColumn("Telefone");
            TabelaClientes.setModel(modelTabelClientes);

            modelTabelClientes.setRowCount(0);

            for (Cliente c : listaClientes) {
                modelTabelClientes.addRow(new Object[]{c.getCpf(),c.getNome(),c.getEmail(), c.getTelefone()});
            }
    }
